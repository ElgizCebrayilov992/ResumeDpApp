package com.mycompany.entity;

/**
 *
 * @author virtu
 */
public class Skill {

    private int id;
    private String Name;

    public Skill(int id, String Name) {
        this.id = id;
        this.Name = Name;
    }

    public Skill() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return Name;
    }

    public void setName(String Name) {
        this.Name = Name;
    }

    @Override
    public String toString() {
        return  Name;
    }

    
}
